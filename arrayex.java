public class arrayex {

    public static void main(String args[]){
        int[] marks=new int[10];
        marks[0]=20;
        marks[2]=300;
        marks[3]=200;
        marks[4]=250;
        System.out.println(marks[0]);
        System.out.println(marks);

      float[] arr=new float[5];
      arr[0]=20.0f;
      System.out.println(arr);
      System.out.println(arr[0]);

      String[] namesoff={"hello","my","name","kumar"};
      System.out.println(namesoff[1]);
      System.out.println(namesoff[2]);
      System.out.println(namesoff[3]);
      System.out.println(namesoff.length);
    }
}
